# Bitwise Operators
def bitwise_operations(a, b):
    print(f"Bitwise Operations on {a} and {b}:")
    print(f"AND: {a} & {b} = {a & b}")
    print(f"OR: {a} | {b} = {a | b}")
    print(f"XOR: {a} ^ {b} = {a ^ b}")
    print(f"NOT: ~{a} = {~a}")
    print(f"Left Shift: {a} << 1 = {a << 1}")
    print(f"Right Shift: {a} >> 1 = {a >> 1}")
    print()
